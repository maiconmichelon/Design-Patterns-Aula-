package facade;

public class BluRayPlayer {

	public void ligar() {
		System.out.println("Ligando Bluray");
	}
	
	public void play() {
		System.out.println("Iniciando filme");
	}
	
	public void stop() {
		System.out.println("Finalizando filme");
	}
	
	public void desligar() {
		System.out.println("Desligando Bluray");
	}
	
}